function isNoise(entry) {
  const text = (entry.title + ' ' + entry.description).toLowerCase();
  // No dataSource = manual entry → always keep
  if (!entry.dataSource) return false;
  // Has dataSource → check noise patterns
  for (const pat of NOISE_PATTERNS) {
    if (pat.test(text)) return true;
  }
  // Very generic business news that isn't about actual trade/BD
  if (entry.industry === '综合商务' && entry.dataSource) {
    // Keep entries with region labels (more specific)
    if (!entry.regionLabel) return true;
  }
  return false;
}


// ── Main ──────────────────────────────────────────────────────────────────
function main() {
  let data = [];
  if (fs.existsSync(DATA_FILE)) {
    try {
      data = JSON.parse(fs.readFileSync(DATA_FILE, 'utf8'));
    } catch (e) {
      console.error('Failed to parse JSON:', e.message);
      process.exit(1);
    }
  }

  const countBefore = data.length;

  // 1. Remove noise entries
  const clean = data.filter(entry => !isNoise(entry));
  const removed = countBefore - clean.length;
  console.log(`Removed ${removed} noise entries (kept ${clean.length} good entries)`);

  // 2. Get existing manual entry IDs (don't duplicate)
  const existingIds = new Set(clean.map(e => e.id));

  // 3. Add new manual entries (skip if already exists)
  let added = 0;
  for (const entry of MANUAL_ENTRIES) {
    if (!existingIds.has(entry.id)) {
      clean.unshift(entry); // add to front
      added++;
    }
  }
  console.log(`Added ${added} new high-quality manual entries`);

  // 4. Sort by publishedAt descending, cap at 500
  clean.sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt));
  const final = clean.slice(0, 500);

  // 5. Write back
  fs.writeFileSync(DATA_FILE, JSON.stringify(final, null, 2), { encoding: 'utf8' });
  console.log(`Final count: ${final.length} entries`);
  console.log(`Breakdown: manual=${final.filter(e => !e.dataSource).length}, auto=${final.filter(e => e.dataSource).length}`);

  // 6. Country stats
  const byCountry = {};
  final.forEach(e => { byCountry[e.country] = (byCountry[e.country] || 0) + 1; });
  console.log('\nBy country:');
  Object.entries(byCountry).sort((a, b) => b[1] - a[1]).forEach(([c, n]) => console.log(`  ${c}: ${n}`));
}

main();
